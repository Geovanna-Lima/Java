package Class5;

public class Calculadora {

	public static Integer somar(Integer s1, Integer s2) {
		return s1 + s2; 
	}
	
	public static Double somar(Double s1, Double s2) {
		return s1 + s2; 
	}
	
	public static Integer quadrado(Integer q1) {
		return q1 * q1; 
	}
	
	public static Double pi() {
		return 3.14; 
	}
		
}
